package Clase_06;

public class Loops {

    public static void main(String[] args) {

        // Loops - Bucles

        // - for
        for (int i = 0; i < 5; i++){
            System.out.println("Hola, Java!");
        }

        //
        String[] names = {"Andres", "Marroquin", "Bernal"};
        for (int i = 0; i < names.length; i++){
            System.out.println(names[i]);
        }
    }
}
